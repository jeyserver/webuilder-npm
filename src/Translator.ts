	private static lang:string;
	public static is_shortCode(code:string):boolean{
		for(var i = 0;i<this.allowlangs.length;i++){
			if(code == this.allowlangs[i]){
				return true;
			}
		}
		return false;
	}
	public static is_validCode(code:string):boolean{
		let matches;
		if(matches = code.match(/^([a-z]{2})_([A-Z]{2})$/)){
			if(this.is_shortCode(matches[1])){
				for(var i = 0;i<this.countries.length;i++){
					if(matches[2] == this.countries[i]){
						return true;
					}
				}
			}
		}
		return false;
	}
